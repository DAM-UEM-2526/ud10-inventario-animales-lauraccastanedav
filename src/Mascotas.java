
public abstract class Mascotas {

	protected String nombre;
	protected int edad;
	protected String estado;
	protected String fechaNac;
	
	public Mascotas(String nombre, int edad, String estado, String fechaNac) {
		this.nombre = nombre;
		this.edad = edad;
		this.estado = estado;
		this.fechaNac = fechaNac;
	}
	
	
	

}
